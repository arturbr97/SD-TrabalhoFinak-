    private void jBSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBSubmitActionPerformed
        // TODO add your handling code here:

        if (jTIP.getText().isEmpty() || jTPort.getText().isEmpty()
                || jTKeywords.getText().isEmpty() || this.arquivoSelected == null) {
            JOptionPane.showMessageDialog(rootPane, "Fill in all the fields, and select file text");
        } else {
            try {
                FileManager fm = new FileManager(this.arquivoSelected.getAbsolutePath());
                DataTransferModel dtm = new DataTransferModel();
                dtm.setKeywords(jTKeywords.getText());
                dtm.setText(fm.readData());
                String dataForTrasn = Crypto64.encode(new Gson().toJson(dtm));

                String ip = jTIP.getText();
                int port = Integer.parseInt(jTPort.getText());

                SocketWriteMessage message = new SocketWriteMessage(new Socket(ip, port));
                message.write(dataForTrasn);
                //Pega o timestamp inicial
                this.fm.write(String.valueOf(System.currentTimeMillis()));
            } catch (IOException ex) {
                JOptionPane.showMessageDialog(rootPane, ex.getMessage(), null, JOptionPane.ERROR_MESSAGE, null);
                System.err.println("Erro: " + ex.getMessage());
            }

        }

    }//GEN-LAST:event_jBSubmitActionPerformed

